

function runIt(dta)
    pos = 1
    cmd = dta[pos]

    while(cmd != 99)

        op1 = dta[dta[pos+1]+1]
        op2 = dta[dta[pos+2]+1]

        if(cmd == 1)
            res = op1 + op2
        elseif(cmd == 2)
            res = op1 * op2
        end
        dta[dta[pos+3]+1] = res
        pos += 4
        cmd = dta[pos]
    end
end

